package Intermediario.Construtores;

public class Hokage {
    String nome;
    int idade;
    boolean vivoOuNao;
    String aldeia;
    int missoes;
    double saldo;
    double altura;

    public Hokage(String nome, int idade, boolean vivoOuNao, String aldeia, int missoes, double saldo, double altura) {
        this.nome = nome;
        this.idade = idade;
        this.vivoOuNao = vivoOuNao;
        this.aldeia = aldeia;
        this.missoes = missoes;
        this.saldo = saldo;
        this.altura = altura;
    }
}
